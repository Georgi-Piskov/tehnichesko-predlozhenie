# ===== FORWARD-PASS SCHEDULING =====
n = len(schedule_data)
start_day = [0] * n
end_day = [0] * n

is_summary = []
for i, (indent, name, unit, qty, dur, preds, res) in enumerate(schedule_data):
    is_summary.append(indent == 0)

# Forward pass for work tasks
for i in range(n):
    if is_summary[i]:
        continue
    indent, name, unit, qty, dur, preds, res = schedule_data[i]
    if not preds:
        start_day[i] = 1
    else:
        max_pred_end = 0
        for p in preds:
            pidx = p - 1
            if end_day[pidx] > max_pred_end:
                max_pred_end = end_day[pidx]
        start_day[i] = max_pred_end + 1
    end_day[i] = start_day[i] + int(math.ceil(dur)) - 1

# Summary tasks = span of children
def find_children(idx):
    s_indent = schedule_data[idx][0]
    children = []
    for j in range(idx + 1, n):
        if schedule_data[j][0] <= s_indent:
            break
        if not is_summary[j]:
            children.append(j)
    return children

for i in range(n):
    if not is_summary[i]:
        continue
    ch = find_children(i)
    if ch:
        start_day[i] = min(start_day[c] for c in ch)
        end_day[i] = max(end_day[c] for c in ch)

actual_end = max(end_day)
print(f"Критичен път: {actual_end} работни дни")

# Print schedule
print(f"\n{'ID':>3} {'Нач':>4} {'Край':>4} {'Дни':>4}  Задача")
print("-" * 90)
for i in range(n):
    indent = schedule_data[i][0]
    name = schedule_data[i][1]
    dur = schedule_data[i][3]
    prefix = "  " * indent
    s = "*" if is_summary[i] else " "
    actual_dur = end_day[i] - start_day[i] + 1 if is_summary[i] else dur
    short_name = name[:60]
    print(f"{i+1:>3} {start_day[i]:>4} {end_day[i]:>4} {actual_dur if actual_dur else '':>4}  {s}{prefix}{short_name}")

